class LRUCache {
    class Node{
        private int key;
        private int value;
        private Node prev = null;
        private Node next = null;

        public Node(int key, int value){
            this.key = key;
            this.value = value;
        }

        public Node getPrev(){
            return prev;
        }

        public Node getNext(){
            return next;
        }

        public int getKey(){
            return key;
        }

        public int getValue(){
            return value;
        }

        public void setPrev(Node prev){
            this.prev = prev;
        }

        public void setNext(Node next){
            this.next = next;
        }

        public void setValue(int value){
            this.value = value;
        }
    }

    private HashMap<Integer, Node> cache = new HashMap<Integer, Node>();
    private int size;
    private int capacity;
    private Node head;
    private Node tail;

    public LRUCache(int capacity) {
        this.capacity = capacity;
        size = 0;
        head = new Node(-1, -1);
        tail = new Node(-1, -1);
        head.setNext(tail);
        tail.setPrev(head);
    }

    public int get(int key) {
        if (!cache.containsKey(key)) {
            return -1;
        }
        Node curr = cache.get(key);
        moveToFirst(curr);
        return curr.getValue();
    }

    public void put(int key, int value) {
        if (!cache.containsKey(key)) {
            Node curr = new Node(key, value);
            size++;
            cache.put(key, curr);
            head.getNext().setPrev(curr);
            curr.setNext(head.getNext());
            curr.setPrev(head);
            head.setNext(curr);
            if (size > capacity){
                removeLast();
                size--;
            }
        }else{
            Node curr = cache.get(key);
            curr.setValue(value);
            moveToFirst(curr);
        }
    }

    private void moveToFirst(Node curr){
        curr.getPrev().setNext(curr.getNext());
        curr.getNext().setPrev(curr.getPrev());
        head.getNext().setPrev(curr);
        curr.setNext(head.getNext());
        curr.setPrev(head);
        head.setNext(curr);
    }

    private void removeLast(){
        Node last = tail.getPrev();
        cache.remove(last.getKey());
        last.getPrev().setNext(tail);
        tail.setPrev(last.getPrev());
    }
}

