/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
    ListNode* partition(ListNode* head, int x) {
        ListNode* newHead = new ListNode();
        newHead->next = head;
        ListNode* prev = newHead;
        ListNode* temp = head;
        ListNode* curr = newHead;
        while (temp != NULL) {
            if (temp->val < x){
                if (curr->next != temp){
                    ListNode* tem = curr->next;
                    curr->next = temp;
                    prev->next = temp->next;
                    temp->next = tem;
                }
                curr = curr->next;
            }
            prev = temp;
            temp = temp->next;
        }
        return newHead->next;
    }
};
