/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
    ListNode* removeNthFromEnd(ListNode* head, int n) {
        ListNode* temp1 = head;
        for (int i=0; i<n; i++) {
           temp1 = temp1->next;
        }
        if (temp1 == NULL)
            return head->next;
        ListNode* temp2 = head;
        while (temp1->next != NULL) {
            temp1 = temp1->next;
            temp2 = temp2->next;
        }
        temp2->next = temp2->next->next;
        return head;
    }
};
